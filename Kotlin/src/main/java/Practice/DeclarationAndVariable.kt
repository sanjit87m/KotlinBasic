package Practice
/**
 * val - Immutable reference
 * var - Mutable reference
 * To declare a variable use var or val keyword
 *  Declaration of data type of variable is not mandatory
 *
 *  The basic data type in Kotlin can be categorized as:
 *  1. Number 2. Character 3. Boolean 4. Arrays 5. String
 * */
fun main(args: Array<String>){
    var number1: Int = 10
    var number2: Double = 100.00
    var number3: Float = 100.0f
    var number4: Long = 10000000
    var number5: Short = 10
    var number6: Byte = 1

    println("Your Int Value is :$number1")
    println("Your Double value is : $number2")
    println("Your Float value is : $number3")
    println("Your Long value is :$number4")
    println("Your Short value is : $number5")
    println("Your Byte value is: $number6")


}