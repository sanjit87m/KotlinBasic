package com.fromfuture.kotlin.Basic

/**
 *  ****** Variable
 * Val - Immutable
 * Var - Mutable
 *
 *
 * ***** Data Type *******
 *  1. Number
 *  2. Character
 *  3. Boolean
 *  4. Array
 *  5. String
 */

fun main(){
    var number1: Int = 10
    var number2: Float = 2.3f
    var number3: Double = 100.00
    var number4: Long = 100000
    var number5: Short = 10
    var number6: Byte = 1

    println("Your int value is $number1")
    println("Your Double value is $number3")
    println("Your Float value is $number2")
    println("Your Long value is $number4")
    println("Your Short value is $number5")
    println("Your Byte value is $number6")


}