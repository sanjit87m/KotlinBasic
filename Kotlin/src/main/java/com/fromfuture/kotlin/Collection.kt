/*
------------------Collections -----------------------
1. List
2. Set
3. Map

Each collection is of two types
1. Immutable (read only)
2. Mutable (read-write)

List - 1. An ordered collection 2. Elements can be repeated
Immutable method  -listOf()
Mutable methods - ArrayList<T>(),arrayListOf(), mutableListOf()

Set - 1. Collection of un order elements 2. Collection of unique elements

Immutable methods - setOf()
Mutable methods - hashSetOf(), mutableSetOf()

Map - 1.Set of key-value pairs , Values - duplicate , Keys - unique
Immutable methods - mapOf()
Mutable methods - HashMap<K,V>(), hashMapOf(), mutableMapOf()

 */




















